// Quando você precisa implementar uma tarefa de programação, a Biblioteca de Classes do .NET é um bom 
// lugar para procurar, pois é uma coleção organizada de recursos de programação.
// A Biblioteca de Classes do .NET é uma coleção de milhares de classes que contém dezenas de milhares 
// de métodos. Por exemplo, ela inclui a classe Console para desenvolvedores que trabalham em 
// aplicativos de console. A classe Console inclui métodos para operações de entrada e saída, 
// como Write(), WriteLine(), Read(), ReadLine() e muitos outros. Por exemplo:
Console.WriteLine("Hello, World!")

// Pense em uma classe como um contêiner para métodos ou como as seções da biblioteca pública. Os 
// desenvolvedores normalmente mantêm os métodos relacionados juntos em uma única classe. Como você 
// viu no exemplo anterior, todos os métodos que podem enviar ou receber informações de uma janela de 
// console são coletados na classe System.Console da biblioteca de classes do .NET.
// Existem outras classes com métodos que dão suporte de uma forma mais geral. Em outras palavras, sua 
// utilidade abrange uma ampla variedade de plataformas de dispositivos, estruturas de aplicativos e 
// áreas de tecnologia. Por exemplo, se você quiser ler ou gravar informações de arquivos ou executar 
// operações de trigonometria ou cálculo, há classes de uso geral que podem ser usadas em seu código.
// Não importa se você está criando aplicativos para a Web, a área de trabalho, um dispositivo 
// móvel ou a nuvem, as classes e os métodos de uso geral são uma opção útil.




// ATÉ MESMO OS TIPOS DE DADOS FAZEM PARTE DA BIBLIOTECA DE CLASSES DO .NET
// Na verdade, os tipos de dados em C# (como string e int) são disponibilizados por meio de classes 
// na biblioteca de classes do .NET. A linguagem C# mascara a conexão entre os tipos de dados e as 
// classes do .NET para simplificar seu trabalho. No entanto, nos bastidores, os tipos de dados são 
// implementados como todas as outras classes na Biblioteca de Classes do .NET. Essa conexão fornece 
// suas variáveis cotidianas com métodos internos que podem ser muito úteis. A classe string tem muitos 
// desses métodos úteis. Por exemplo, a classe string tem métodos para converter texto em letras 
// maiúsculas e minúsculas (ToUpper e ToLower).




